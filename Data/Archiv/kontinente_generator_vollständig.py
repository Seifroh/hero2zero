# === SQL-Zeilen generieren ===
sql_lines = []

for _, row in df.iterrows():
    land = str(row['country']).strip()
    kontinent = continent_mapping.get(land)
    if kontinent:
        sql = f"UPDATE countryemission SET continent = '{kontinent}' WHERE country = '{land}';"
        sql_lines.append(sql)

